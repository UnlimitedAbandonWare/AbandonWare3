$TempRoot = Join-Path ([System.IO.Path]::GetTempPath()) ("awx-learning-ops-collector-smoke-" + [guid]::NewGuid().ToString("N"))
New-Item -ItemType Directory -Force -Path $TempRoot | Out-Null
