function Get-SmokeGradleArguments {
    $args = @("bootRun", "--rerun-tasks", "--no-daemon", "-x", "test")
    if (-not [string]::IsNullOrWhiteSpace($env:AWX_PROJECT_CACHE_DIR)) {
        $args += @("--project-cache-dir", $env:AWX_PROJECT_CACHE_DIR)
    }
    return $args
}

function Assert-NoGradleCollision([string]$Stage) {
    $rootPattern = [regex]::Escape($Root)
    $conflicts = @(Get-CimInstance Win32_Process -ErrorAction SilentlyContinue |
            Where-Object {
                $_.ProcessId -ne $PID -and
                $_.CommandLine -match $rootPattern -and
                ($_.CommandLine -match 'gradlew\.bat|GradleWrapperMain|GradleMain|bootRun' -or
                 $_.CommandLine -match 'build\\classes\\java\\main.*com\.example\.lms\.LmsApplication')
            } |
            Select-Object -First 5 -Property ProcessId, Name, CommandLine)
    if ($conflicts.Count -gt 0) {
        $summary = ($conflicts | ForEach-Object { "pid=$($_.ProcessId) name=$($_.Name)" }) -join "; "
        throw "[AWX][learning-ops-smoke] gradle-cache-collision stage=$Stage conflictingProcesses=$summary"
    }
}

function Start-SmokeApp {
    Assert-NoGradleCollision "before-bootRun"
    $outLog = Join-Path $TempRoot "app.out.log"
    $errLog = Join-Path $TempRoot "app.err.log"
    $proc = Start-Process -FilePath $Gradle `
        -ArgumentList (Get-SmokeGradleArguments) `
        -WorkingDirectory $Root `
        -RedirectStandardOutput $outLog `
        -RedirectStandardError $errLog `
        -WindowStyle Hidden `
        -PassThru
    return @{ Process = $proc; OutLog = $outLog; ErrLog = $errLog }
}

function Stop-SmokeApp($App) {
    if ($null -eq $App -or $null -eq $App.Process) { return }
    $proc = $App.Process
    if (-not $proc.HasExited) {
        & taskkill.exe /PID $proc.Id /T /F | Out-Null
        $proc.WaitForExit(10000) | Out-Null
    }
}

function Tail-Log($Path) {
    if (-not (Test-Path $Path)) { return "" }
    return Redact-Text ((Get-Content -LiteralPath $Path -Tail 100 -ErrorAction SilentlyContinue) -join [Environment]::NewLine)
}

function Wait-ForHttp($App) {
    $deadline = (Get-Date).AddSeconds($StartupTimeoutSeconds)
    $url = "http://127.0.0.1:$ManagementPort/actuator/health"
    while ((Get-Date) -lt $deadline) {
        if ($App.Process.HasExited) {
            throw "[AWX][learning-ops-smoke] app exited during startup`n$(Tail-Log $App.OutLog)`n$(Tail-Log $App.ErrLog)"
        }
        try {
            Invoke-WebRequest -Uri $url -UseBasicParsing -TimeoutSec 2 | Out-Null
            return
        } catch {
            Start-Sleep -Seconds 2
        }
    }
    throw "[AWX][learning-ops-smoke] startup timeout`n$(Tail-Log $App.OutLog)`n$(Tail-Log $App.ErrLog)"
}

function Invoke-Json([string]$Path) {
    $resp = Invoke-WebRequest -Uri "http://127.0.0.1:$Port$Path" -UseBasicParsing -TimeoutSec 15
    return @{ Status = [int]$resp.StatusCode; Body = ($resp.Content | ConvertFrom-Json) }
}

function Assert-Eq($Actual, $Expected, [string]$Label) {
    if ([string]$Actual -ne [string]$Expected) {
        throw "[AWX][learning-ops-smoke] $Label expected=$Expected actual=$Actual"
    }
}

function Assert-True($Condition, [string]$Label) {
    if (-not $Condition) {
        throw "[AWX][learning-ops-smoke] assertion failed: $Label"
    }
}

function Wait-ForCollectorFile {
    $deadline = (Get-Date).AddSeconds($CollectorTimeoutSeconds)
    while ((Get-Date) -lt $deadline) {
        if ((Test-Path $CollectorFile) -and ((Get-Item $CollectorFile).Length -gt 0)) {
            return
        }
        Start-Sleep -Seconds 2
    }
    $collectorOutputName = Split-Path -Leaf $CollectorFile
    throw "[AWX][learning-ops-smoke] collector output timeout outputFileHash=$(Hash-Value $collectorOutputName) outputFileLength=$($collectorOutputName.Length)"
}

$app = $null
try {
    New-SeedDatabase
    Set-SmokeEnv
    $app = Start-SmokeApp
    Wait-ForHttp $app

    Wait-ForCollectorFile
    $overview = Invoke-Json "/api/diagnostics/rag/learning-ops/overview?limit=20"
    $loop = Invoke-Json "/api/diagnostics/uaw/autolearn/loop"

    Assert-Eq $overview.Status 200 "overview status"
    Assert-Eq $loop.Status 200 "loop status"
    Assert-Eq $overview.Body.learningOpsCollector.enabled $true "collector enabled"
    Assert-Eq $overview.Body.learningOpsCollector.status "ready_read_only_curation_queue" "collector status"
    Assert-Eq $overview.Body.learningOpsCollector.writesDataset $false "collector writesDataset"
    Assert-Eq $overview.Body.learningOpsCollector.requiresReview $true "collector requiresReview"
    $collectorOutputName = Split-Path -Leaf $CollectorFile
    $collectorProps = @($overview.Body.learningOpsCollector.PSObject.Properties.Name)
    Assert-True (-not ($collectorProps -contains "outputFile")) "collector omits raw output file"
    Assert-Eq $overview.Body.learningOpsCollector.outputFileHash (Hash-Value $collectorOutputName) "collector output file hash"
    Assert-Eq $overview.Body.learningOpsCollector.outputFileLength $collectorOutputName.Length "collector output file length"
    Assert-Eq $overview.Body.learningOpsCollector.outputPathHash (Hash-Value $CollectorFile) "collector output hash"
    Assert-Eq $overview.Body.opsLedger.datasetPipelineQueue.status "ready" "queue status"
    Assert-True ([int]$overview.Body.opsLedger.datasetPipelineQueue.itemCount -ge 1) "queue has candidates"
    Assert-Eq $overview.Body.opsLedger.datasetPipelineQueue.items[0].action "demote_heavy_work" "queue action"
    Assert-Eq $loop.Body.nodeProfile.node.role "macmini-control-plane" "loop node role"
    Assert-Eq $loop.Body.nodeProfile.learningLoop.learningOpsCollectorEnabled $true "loop collector enabled"
    Assert-Eq $loop.Body.nodeProfile.selfLearningBridge.datasetPipeline.collectorOutputPathHash (Hash-Value $CollectorFile) "loop collector hash"

    $lastLine = Get-Content -LiteralPath $CollectorFile -Tail 1
    $record = $lastLine | ConvertFrom-Json
    Assert-Eq $record.checkpoint "[AWX][learning-ops][collector]" "jsonl checkpoint"
    Assert-Eq $record.writesDataset $false "jsonl writesDataset"
    Assert-Eq $record.requiresReview $true "jsonl requiresReview"
    Assert-True ([int]$record.itemCount -ge 1) "jsonl has items"
    Assert-Eq $record.items[0].action "demote_heavy_work" "jsonl action"
    Assert-Eq $record.items[0].gpuAdmission.hardwareReason "gpu_memory_pressure" "jsonl gpu reason"

    $combined = ($overview.Body | ConvertTo-Json -Depth 20 -Compress) +
            ($loop.Body | ConvertTo-Json -Depth 20 -Compress) +
            (Get-Content -LiteralPath $CollectorFile -Raw)
    foreach ($forbidden in @($SeedSecret, $TempRoot, $DbFile, $CollectorFile)) {
        if ($combined.Contains($forbidden)) {
            throw "[AWX][learning-ops-smoke] leaked forbidden smoke value: $(Redact-Text $forbidden)"
        }
    }

    Write-Host "[AWX][learning-ops-smoke] OK node=macmini-control-plane collector=written queue=ready writesDataset=false requiresReview=true outputHash=$((Hash-Value $CollectorFile))"
} finally {
    Stop-SmokeApp $app
    Clear-SmokeEnv
    Remove-Item -LiteralPath $TempRoot -Recurse -Force -ErrorAction SilentlyContinue
}
