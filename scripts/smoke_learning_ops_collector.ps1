function Redact-Text([string]$Text) {
    if ($null -eq $Text) { return "" }
    $out = $Text
    foreach ($secret in @($TempRoot, $DbFile, $CollectorFile, $SeedSecret)) {
        if (-not [string]::IsNullOrWhiteSpace($secret)) {
            $out = $out -replace [regex]::Escape($secret), "<redacted>"
        }
    }
    return $out
}

function Hash-Value([string]$Value) {
    if ([string]::IsNullOrWhiteSpace($Value)) { return "" }
    $sha = [System.Security.Cryptography.SHA256]::Create()
    try {
        $bytes = [System.Text.Encoding]::UTF8.GetBytes($Value.Trim())
        $hash = $sha.ComputeHash($bytes)
        $hex = -join ($hash | ForEach-Object { $_.ToString("x2") })
        return "hash:" + $hex.Substring(0, 12)
    } finally {
        $sha.Dispose()
    }
}

function Find-H2Jar {
    $candidates = @(
        "D:\gradle-cache\caches\modules-2\files-2.1\com.h2database\h2",
        (Join-Path $env:USERPROFILE ".gradle\caches\modules-2\files-2.1\com.h2database\h2")
    )
    foreach ($root in $candidates) {
        if (Test-Path $root) {
            $jar = Get-ChildItem -LiteralPath $root -Recurse -Filter "h2-*.jar" -ErrorAction SilentlyContinue |
                    Sort-Object FullName -Descending |
                    Select-Object -First 1
            if ($null -ne $jar) {
                return $jar.FullName
            }
        }
    }
    throw "[AWX][learning-ops-smoke] evidence_needed: H2 runtime jar missing / run .\gradlew.bat test or verify Gradle cache"
}

function Sql-String([string]$Value) {
    return "'" + ($Value -replace "'", "''") + "'"
}

function New-SeedDatabase {
    $h2 = Find-H2Jar
    $seedSql = Join-Path $TempRoot "seed-rag-ops-ledger.sql"
    $sourceCounts = @{ attempted = 3; accepted = 1; resultCount = 0 } | ConvertTo-Json -Compress
    $quality = @{
        trainDecision = "BLOCK_RETRAIN"
        vectorDecision = "REVIEW"
        rawQuery = $SeedSecret
    } | ConvertTo-Json -Compress
    $matrix = @{
        "q_gpu_hardware_pressure" = 0.71
        "uaw.gpu-hardware.status" = "ok"
        "uaw.gpu-hardware.admission.status" = "degraded"
        "uaw.gpu-hardware.admission.reason" = "gpu_memory_pressure"
        "uaw.gpu-hardware.admission.pressureLevel" = "warn"
        "uaw.gpu-hardware.admission.retrainAllowed" = $false
        "ownerToken" = $SeedSecret
    } | ConvertTo-Json -Compress
    $sql = @"
CREATE TABLE IF NOT EXISTS rag_ops_ledger (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  version BIGINT NULL,
  run_id VARCHAR(96) NOT NULL,
  entry_type VARCHAR(32) NOT NULL,
  session_hash VARCHAR(64) NULL,
  request_hash VARCHAR(64) NULL,
  query_hash VARCHAR(64) NULL,
  query_length INT NULL,
  plan_id VARCHAR(128) NULL,
  strategy_name VARCHAR(128) NULL,
  resource_tier VARCHAR(32) NULL,
  source_counts_json CLOB NULL,
  quality_json CLOB NULL,
  vector_json CLOB NULL,
  kg_json CLOB NULL,
  matrix_json CLOB NULL,
  decision VARCHAR(32) NULL,
  failure_class VARCHAR(64) NULL,
  hotspot VARCHAR(64) NULL,
  latency_ms BIGINT NULL,
  created_at TIMESTAMP NOT NULL
);
INSERT INTO rag_ops_ledger (
  run_id, entry_type, session_hash, request_hash, plan_id, strategy_name, resource_tier,
  source_counts_json, quality_json, vector_json, kg_json, matrix_json,
  decision, failure_class, hotspot, latency_ms, created_at
) VALUES (
  'collector-smoke-run', 'AUTOLEARN_CYCLE', 'hash-session', 'hash-request',
  'autolearn', 'uaw-autolearn', 'background',
  $(Sql-String $sourceCounts), $(Sql-String $quality), '{}', '{}', $(Sql-String $matrix),
  'BLOCK_RETRAIN', 'gpu_hardware_pressure', 'gpu_hardware', NULL, CURRENT_TIMESTAMP()
);
"@
    [System.IO.File]::WriteAllText($seedSql, $sql, [System.Text.UTF8Encoding]::new($false))
    & java -cp $h2 org.h2.tools.RunScript -url $DbUrl -user sa -script $seedSql
    if ($LASTEXITCODE -ne 0) {
        throw "[AWX][learning-ops-smoke] H2 seed failed exit=$LASTEXITCODE"
    }
}

